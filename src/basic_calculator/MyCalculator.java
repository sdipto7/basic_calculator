    public void calculation() {
        try {
            if (operation == 1) {
                String str = display.getText();
                answer = number + Double.parseDouble(str);
            } else if (operation == 2) {
                String str = display.getText();
                answer = number - Double.parseDouble(str);
            } else if (operation == 3) {
                String str = display.getText();
                answer = number * Double.parseDouble(str);
            } else if (operation == 4) {
                String str = display.getText();
                answer = number / Double.parseDouble(str);
            } else {
                String str = display.getText();
                answer = Double.parseDouble(str);
            }
            display.setText(Double.toString(answer));
        } catch (Exception e) {
        } finally {
            answer = 0.0;
            number = 0;
            operation = 0;
        }
    }

