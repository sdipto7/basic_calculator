    //"/" --> DIVISION BUTTON
    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        if (!operator) {  //to check if the format is just a number
            try {
                operation = 4;
                operator = true;
                String str = display.getText();
                number = Double.parseDouble(str);
                label.setText(str + "/");
                display.setText("");
            } catch (Exception e) {
            }
        } else { //to check if the format is number+ or number* or number- or number/
            if (display.getText().equals("")) { //if no second number on display then just change the operator symbol
                operation = 4;
                String str = label.getText();
                int lastChar = str.length() - 1;
                if (str.length() > 0) {
                    StringBuilder builder = new StringBuilder(str);
                    builder.deleteCharAt(lastChar);
                    number = Double.parseDouble(builder.toString());
                    label.setText(builder.toString() + "/");
                    display.setText("");
                }
            } else { //if there is something on the display then pressing an operator will be caluclated and the answer will have the operator symbol
                calculation();
                String text = display.getText();
                label.setText(text + "/");
                display.setText("");
                operation = 4;
                operator = true;
                number = Double.parseDouble(text);
            }
        }
    }//GEN-LAST:event_jButton15ActionPerformed

